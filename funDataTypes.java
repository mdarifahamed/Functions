import java.util.*;
public class funDataTypes {
    // add two int value
    public static int sum(int a, int b){
        return a+b;
    }
    // add two float value
    public static float sum(float a, float b){
        return a+b;
    }
    public static void main(String[] args) {
        System.out.println(sum(4,8));
        System.out.println(sum(4.5f, 7.5f));
    }
}
